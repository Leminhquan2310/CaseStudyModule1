class Product {
  constructor(id, category_id, brand_id, name, price, image, quantity, description) {
    this.id = id;
    this.category_id = category_id;
    this.brand_id = brand_id;
    this.name = name;
    this.price = price;
    this.image = image;
    this.quantity = quantity;
    this.description = description;
  }
}
